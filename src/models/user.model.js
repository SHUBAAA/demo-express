import mongoose from 'mongoose';

const estudianteSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    dni: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const EstudianteModel = mongoose.model('Estudiante', estudianteSchema);

export default EstudianteModel;
